 window.onscroll = function() {scrollFunction()};

    function scrollFunction() {
        var wt = $(window).scrollTop() + 140;
        $('.aboutus-title a').removeClass('active');
        if(wt > $('#history').position().top){
            $('.aboutus-title a[href="#history"]').addClass('active');
        }else if(wt > $('#news').position().top){
            $('.aboutus-title a[href="#news"]').addClass('active');
        }else if(wt > $('#purpose-vision').position().top){
            $('.aboutus-title a[href="#purpose-vision"]').addClass('active');
        }else if(wt > $('#message').position().top){
            $('.aboutus-title a[href="#message"]').addClass('active');
        }else if(wt > $('#aboutus').position().top){
            $('.aboutus-title a[href="#aboutus"]').addClass('active');
        }
    }

  $(window).scroll(fixDiv);
  fixDiv();
    $(".aboutus-title a").click(function() {
           $(".aboutus-title a").removeClass("active");
           var id = $(this).attr('href');
           $(this).addClass("active");
           $('html, body').animate({
                    scrollTop: ($(id).offset().top)-100
                }, 1000);
    });
